#include "fillit.h"

void    clearmap(char **map)
{
    int y;
    
    y = 0;
    while (*map)
    {
        while ((*map)[y] != 0)
        {
            if (ft_isalpha((*map)[y]))
                (*map)[y] = '.';
            y++;
        }
        y = 0;
        map++;
    }
}